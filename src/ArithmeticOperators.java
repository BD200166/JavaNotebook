
public class ArithmeticOperators {

	public static void main(String[] args) {
		
		int number1 = 1 + 2;  
		System.out.println(number1);
								//Arithmetic Operators
		int number2 = 1;
		int number3 = 2;				//[+] Addition -> number = 1 + 2;
		int result = number2 + number3;
		System.out.println(result);			//[-] Subtraction -> number = 1 - 2;
		
		int number4 = 1;				//[*] Multiplication -> number = 1 * 2;
		int number5 = 2;
		System.out.println(number4 + number5);		//[/] Division -> number = 1 / 2;
		
		int number6 = 1;
		int number7 = 2;
		System.out.println(number6 + "" + number7);
	}										
	
}
